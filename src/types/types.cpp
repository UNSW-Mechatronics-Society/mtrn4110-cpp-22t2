#include <iostream>
#include <string>

int main(void) {
    // `int` for an integer
    int a = 1;

    // `double` for a rational number
    double b = 1.0;

    // `char` for a single character
    char c = 'c';

    // `bool` for a boolean (true/false)
    bool d = true;

    // strings for an array of characters
    std::string e = "mtrn4110";

    std::cout << a << std::endl;
    std::cout << b << std::endl;
    std::cout << c << std::endl;
    std::cout << d << std::endl;
    std::cout << e << std::endl;
}